-- ============================================================================
-- 数据库迁移：添加印章 URL 字段支持
-- ============================================================================
-- 
-- 功能说明：
-- 为 certificates 表添加 seal_url 字段来存储用户上传的自定义印章 URL。
-- 这样可以区分用户上传的自定义印章和模板提供的默认印章。
--
-- 修复的问题：
-- 之前的系统中，用户上传的自定义印章虽然被渲染到证书中，但没有被单独跟踪。
-- 在重新查看/编辑证书时，系统只能使用模板的默认印章（stamp_url），
-- 导致用户上传的自定义印章丢失。
--
-- 解决方案：
-- 1. 添加 seal_url 字段到 certificates 表
-- 2. 在核发/更新证书时，保存用户上传的自定义印章 URL
-- 3. 在查看/编辑证书时，优先使用 seal_url，降级到 templates.stamp_url
--
-- ============================================================================

-- 步骤 1: 添加 seal_url 列到 certificates 表（如果不存在）
ALTER TABLE certificates 
ADD COLUMN seal_url TEXT DEFAULT NULL;

-- 步骤 2: 添加表注释说明
COMMENT ON COLUMN certificates.seal_url IS '用户上传的自定义印章 URL（核发时保存。下载时优先使用，若为空则使用 templates.stamp_url）';

